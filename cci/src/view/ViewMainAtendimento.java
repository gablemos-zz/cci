    private void jtClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtClientesMouseClicked
        if (evt.getClickCount() == 2) {
            
            ServicoDAO sdao = new ServicoDAO();
            
            for (Servico  s: sdao.buscarId(Integer.parseInt(jtClientes.getValueAt(jtClientes.getSelectedRow(), 0).toString()))) {
                        lblId.setText(Integer.toString(s.getId()));
                        lblFCpf.setText(s.getFuncionario().getCpf());
                        lblCCpf.setText(s.getCliente().getCpf());
                
            }
            
            new ViewServico(lblCCpf.getText(), lblFCpf.getText(), Integer.parseInt(lblId.getText())).setVisible(true);
        }
        
        
    }//GEN-LAST:event_jtClientesMouseClicked

