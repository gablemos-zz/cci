    public void readJTable(){
        DefaultTableModel modelo = (DefaultTableModel) jtClientes.getModel();
        modelo.setNumRows(0);
        ServicoDAO dao = new ServicoDAO();
        
        for(Servico s: dao.buscarTodosAbertos()){
            modelo.addRow(new Object[]{
                s.getId(),
                s.getData(),
                s.getHora(),               
                s.getDescicao()});
        }
    }

