    public void readJTable(){
        DefaultTableModel modelo = (DefaultTableModel) jtFornecedores.getModel();
        modelo.setNumRows(0);
        FornecedorDAO dao = new FornecedorDAO();
        
        for(Fornecedor f: dao.buscarTodos()){
            modelo.addRow(new Object[]{
                f.getCnpj(),
                f.getNfantasia(),
                f.getTelefone(),
                f.getContato()});
        }
    }
    
