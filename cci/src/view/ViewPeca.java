    public void readJTableFornecedores(){
        DefaultTableModel modelo = (DefaultTableModel) jtFornecedores.getModel();
        modelo.setNumRows(0);
        FornecedorDAO dao = new FornecedorDAO();
        
        for(Fornecedor f: dao.buscarTodos()){
            modelo.addRow(new Object[]{
                f.getCnpj(),
                f.getRsocial(),
                f.getNfantasia()});
        }
    }
    
    public void readJTableFornecedoresCNPJ(String cnpj){
        DefaultTableModel modelo = (DefaultTableModel) jtFornecedores.getModel();
        modelo.setNumRows(0);
        FornecedorDAO dao = new FornecedorDAO();
        
        for(Fornecedor f: dao.buscarCPF(cnpj)){
            modelo.addRow(new Object[]{
                f.getCnpj(),
                f.getRsocial(),
                f.getNfantasia()});
        }
    }
    
    public void readJTablePecas(){
        DefaultTableModel modelo = (DefaultTableModel) jtPecas.getModel();
        modelo.setNumRows(0);
        PecaDAO dao = new PecaDAO();
        
        for(Peca p: dao.buscarTodos()){
            modelo.addRow(new Object[]{
                p.getId(),
                p.getDescricao(),
                p.getValor(),
                p.getFornecedor().getNfantasia()});
        }
    }
    
    public void readJTablePecasNome(String nome){
        DefaultTableModel modelo = (DefaultTableModel) jtPecas.getModel();
        modelo.setNumRows(0);
        PecaDAO dao = new PecaDAO();
        
        for(Peca p: dao.buscarNome(nome)){
            modelo.addRow(new Object[]{
                p.getId(),
                p.getDescricao(),
                p.getValor(),
                p.getFornecedor().getNfantasia()});
        }
    }
    
    public boolean validarPreenchimento() {
        if (txtDescricao.getText().isEmpty()
                || txtUnidade.getText().isEmpty()
                || txtValor.getText().isEmpty()){
            
            JOptionPane.showMessageDialog(null, "Preencha todos os campos");
            return false;
        }else if(txtCNPJ.getText().isEmpty() || txtNomeFantasia.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Selecione um funcionário");
            return false;
        }else{
            return true;
        }
    }

