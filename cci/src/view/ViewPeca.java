    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

        if (validarPreenchimento()) {
            
            FornecedorDAO fdao = new FornecedorDAO();
            PecaDAO pdao = new PecaDAO();
            boolean existeForncedor = fdao.verificarExistencia(txtCNPJ.getText());

            Fornecedor fornecedor = new Fornecedor();
            fornecedor.setCnpj(txtCNPJ.getText());
            

            Peca peca = new Peca(
                    txtDescricao.getText(),
                    txtUnidade.getText(),
                    Double.parseDouble(txtValor.getText()),
                    fornecedor);
            boolean existePeca = false;
            if(!txtCodigo.getText().isEmpty()){
                existePeca = pdao.verificarExistencia(Integer.parseInt(txtCodigo.getText()));            
            }

            if (existePeca && existeForncedor) {
                int resposta = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja alterar este cadastro?", "ALTERAR", JOptionPane.YES_NO_OPTION);

                if (resposta == JOptionPane.YES_OPTION) {
                    peca.setId(Integer.parseInt(txtCodigo.getText()));
                    pdao.alterar(peca);                    
                    readJTableFornecedores();
                    readJTablePecas();
                    JOptionPane.showMessageDialog(null, "Atualizado com sucesso");
                    limparCampos();
                }
            } else {
                pdao.salvar(peca);
                readJTableFornecedores();
                readJTablePecas();
                JOptionPane.showMessageDialog(null, "Cadastro usuário salvo com sucesso");
                limparCampos();
            }

        }

    }//GEN-LAST:event_btnSalvarActionPerformed

