    public boolean validarPreenchimento() {
        if (txtUsuario.getText().isEmpty()|| txtSenha.getText().isEmpty()){
            
            JOptionPane.showMessageDialog(null, "Preencha todos os campos");
            return false;
        }else if(txtCPF.getText().isEmpty() || txtNome.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Selecione um funcionário");
            return false;
        }else{
            return true;
        }
    }

