    public void readJTableFuncionarios(){
        DefaultTableModel modelo = (DefaultTableModel) jtFuncionarios.getModel();
        modelo.setNumRows(0);
        FuncionarioDAO dao = new FuncionarioDAO();
        
        for(Funcionario f: dao.buscarTodos()){
            modelo.addRow(new Object[]{
                f.getCpf(),
                f.getNome(),
                f.getCargo()});
        }
    }
    
