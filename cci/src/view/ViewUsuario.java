    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

        if (validarPreenchimento()) {
            
            String cpf = txtCPF.getText();
            
            UsuarioDAO udao = new UsuarioDAO();
            boolean existeUsuario = udao.verificarExistencia(cpf);

            Funcionario funcionario = new Funcionario();
            funcionario.setCpf(cpf);

            Usuario usuario = new Usuario(
                    txtUsuario.getText(),
                    txtSenha.getText(),
                    funcionario);

            if (existeUsuario) {
                int resposta = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja alterar este cadastro?", "ALTERAR", JOptionPane.YES_NO_OPTION);

                if (resposta == JOptionPane.YES_OPTION) {
                    udao.alterar(usuario);                    
                    readJTableFuncionarios();
                    readJTableUsuarios();
                    JOptionPane.showMessageDialog(null, "Atualizado com sucesso");
                    limparCampos();
                }
            } else {
                udao.salvar(usuario);
                readJTableFuncionarios();
                readJTableUsuarios();
                JOptionPane.showMessageDialog(null, "Cadastro usuário salvo com sucesso");
                limparCampos();
            }

        }

    }//GEN-LAST:event_btnSalvarActionPerformed

