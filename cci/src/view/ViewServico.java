    public void buscarServico() {
        ServicoDAO sdao = new ServicoDAO();
        for (Servico s : sdao.buscarId(Integer.parseInt(txtOrdemServico.getText()))) {
            txtDescricao.setText(s.getDescicao());
            txtData.setText(s.getData());
            txtHora.setText(s.getHora());
            txtCliente.setText(s.getCliente().getNome());
            txtFuncionario.setText(s.getFuncionario().getNome());
        }
    }

