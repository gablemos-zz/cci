    private void jtPecasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtPecasMouseClicked
        if (evt.getClickCount() == 2) {
            int pId = Integer.parseInt(jtPecas.getValueAt(jtPecas.getSelectedRow(), 0).toString());
            
            PecaServicoDAO psdao = new PecaServicoDAO();
            
            Peca peca = new Peca();
            peca.setId(pId);
            
            Servico servico = new Servico();
            servico.setId(sId);
            
            PecaServico pecaservico = new PecaServico(peca, servico);
            if(psdao.salvar(pecaservico)){ 
                readJTablePecaServico();
            }
        }
    }//GEN-LAST:event_jtPecasMouseClicked

