    public void readJTablePecaServico() {
        DefaultTableModel modelo = (DefaultTableModel) jtPecaServico.getModel();
        modelo.setNumRows(0);
        PecaServicoDAO psdao = new PecaServicoDAO();

        for (PecaServico ps : psdao.buscarPorServico(sId)) {
            modelo.addRow(new Object[]{
                ps.getId(),
                ps.getPeca().getDescricao(),
                ps.getPeca().getValor()});
        }
    }    

    public void readJTablePecas() {
        DefaultTableModel modelo = (DefaultTableModel) jtPecas.getModel();
        modelo.setNumRows(0);
        PecaDAO dao = new PecaDAO();

        for (Peca p : dao.buscarTodos()) {
            modelo.addRow(new Object[]{
                p.getId(),
                p.getDescricao(),
                p.getValor(),
                p.getFornecedor().getNfantasia()});
        }
    }

    public void readJTablePecasNome(String nome) {
        DefaultTableModel modelo = (DefaultTableModel) jtPecas.getModel();
        modelo.setNumRows(0);
        PecaDAO dao = new PecaDAO();

        for (Peca p : dao.buscarNome(nome)) {
            modelo.addRow(new Object[]{
                p.getId(),
                p.getDescricao(),
                p.getValor(),
                p.getFornecedor().getNfantasia()});
        }
    }

